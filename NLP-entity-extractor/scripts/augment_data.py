import json
import random
import os
from pathlib import Path

DATASET_FILE = Path("data/train_cls.jsonl")

# Templates extracted from the user's dataset
TEMPLATE_BASE = """Р Е Ш И Л:
Признать должника {fio} несостоятельным (банкротом).
Ввести процедуру реализации имущества сроком на {months} месяцев.
Утвердить финансовым управляющим – {fu_fio}.
Обязать должника передать документы финансовому управляющему.
{target_phrase}
Управлениям Росгвардии – предоставлять по запросу финансового управляющего сведения о снятом с учета оружии.
Филиалам ППК «Роскадастр» – предоставлять сведения о недвижимости.
Управлениям ГИБДД МВД России – предоставлять сведения о транспортных средствах."""

FIO_LIST = ["Танской Елизаветы Александровны", "Иванова Ивана Ивановича", "Петрова Петра Петровича", "Сидорова Алексея Владимировича"]
FU_LIST = ["Закирова Тимура Назифовича", "Смирнова Александра Игоревича", "Кузнецова Дмитрия Сергеевича"]
MONTHS_LIST = ["шесть", "четыре", "пять", "семь", "восемь"]

POSITIVE_PHRASES = [
    # Заблаговременно
    "Обязать финансового управляющего заблаговременно до даты окончания процедуры реализации имущества гражданина представить письменный отчет.",
    "Суд обязывает финансового управляющего заблаговременно предоставить суду отчет о проделанной работе.",
    # За 3 дня
    "Обязать финансового управляющего представить в суд за 3 дня до истечения срока процедуры отчет о результатах.",
    "Не позднее чем за 3 дня до даты судебного заседания финансовый управляющий обязан направить отчет.",
    # За 5 дней
    "Финансовый управляющий обязан за 5 рабочих дней до окончания процедуры представить отчет.",
    "Финансовому управляющему предписывается за 5 дней до истечения срока реализации сдать отчет.",
    # Точная дата
    "Обязать финансового управляющего в срок до 30.09.2026 представить в арбитражный суд отчет о результатах.",
    "Отчет о результатах процедуры финансовый управляющий обязан представить до 15 октября 2026 года.",
    "до 20 декабря 2026 года финансовому управляющему необходимо направить отчет кредиторам и в суд."
]

NEGATIVE_PHRASES = [
    # Не те сроки (за 10 дней, за 15 дней и т.д.)
    "а также представить в суд за 10 рабочих дней до истечения срока процедуры отчет о результатах.",
    "за 15 дней до окончания срока представить отчет о результатах",
    "представить отчет не позднее 20 дней до завершения процедуры",
    # Без указания сроков или стандартные
    "Финансовый управляющий представил суду отчет о результатах процедуры реализации имущества.",
    "После завершения расчетов финансовый управляющий обязан представить в арбитражный суд отчет.",
    "По завершении мероприятий представить в арбитражный суд отчет о своей деятельности.",
    # Кто-то другой (Должник), а не ФУ
    "Обязать должника за 3 дня до истечения срока процедуры представить документы финансовому управляющему.",
    "Должник обязан заблаговременно направить отчет.",
    # Другое действие (не предоставление отчета)
    "Обязать финансового управляющего за 5 дней уведомить кредиторов о проведении собрания.",
    ""  # No mention of report
]

def generate_example(label):
    fio = random.choice(FIO_LIST)
    fu = random.choice(FU_LIST)
    months = random.choice(MONTHS_LIST)
    
    if label == 1:
        phrase = random.choice(POSITIVE_PHRASES)
    else:
        phrase = random.choice(NEGATIVE_PHRASES)
        
    text = TEMPLATE_BASE.format(fio=fio, fu_fio=fu, months=months, target_phrase=phrase)
    return {"text": text, "label": label}

def main():
    generated_examples = []
    
    # Generate 1000 positive and 1000 negative examples
    for _ in range(1000):
        generated_examples.append(generate_example(1))
    for _ in range(1000):
        generated_examples.append(generate_example(0))
        
    random.shuffle(generated_examples)
    
    os.makedirs(DATASET_FILE.parent, exist_ok=True)
    
    with open(DATASET_FILE, "a", encoding="utf-8") as f:
        for ex in generated_examples:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")
            
    print(f"Generated and appended {len(generated_examples)} examples to {DATASET_FILE}")

if __name__ == "__main__":
    main()
